package com.crossent.microservice;

import io.swagger.annotations.ApiModel;

@ApiModel(description="Data")
public class Data {
    String id;
    String name;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
